<h1>Student and Young Scholar Travel grants</h1>
  <br>
    <p style="text-align: justify; text-indent: 2em;">APNet offers several student and young scholar travel grants which cover the APNet conference registration (partially). These grants aim to foster academic exchange and encourage broader participation. All applications will be reviewed based on the available budget and number of applications, which will determine the acceptance criteria.</p>
  <br>
    <p style="text-align: justify; text-indent: 2em;"><strong>New in 2025</strong>, APNet is committed to fostering a more inclusive and diverse community by prioritizing participation from institutions in Southeast Asia and beyond. To support this goal, APNet is offering two distinct travel grant programs:</p>
    <br>
    <ul class="custom-list">
        <li><strong>Standard Travel Grant Program</strong></li>
        <li><strong>Diversity & Inclusion Travel Grant Program</strong></li>
    </ul>
    <br>
    <p style="text-align: justify; text-indent: 2em;">Each program has its own eligibility requirements and application process. Applicants are welcome to choose the program that best suits their profile; however, please note that <strong>each individual may only apply to one</strong> of the two programs.</p>

    <br>
    <h2>Important dates</h2>
    <br>
    <ul class="custom-list">
        <li><strong>Deadline for application:</strong> <del>Saturday, June 14</del> <font color="#FF0000">Wednesday, June 18</font>, 2025</li>
        <li><strong>Notification of acceptance:</strong> <del>Wednesday, June 18</del>  <font color="#FF0000">Thursday, June 19</font>, 2025</li>
    </ul>

    <br>
    <h2>Standard Travel Grant Program</h2>
    
    <h3>Eligibility</h3>
    <br>
    <p style="text-align: justify; text-indent: 2em;">Applicants must meet one of the following criteria:</p>
    <br>
    <ul class="custom-list">
        <li><strong>Students:</strong> The applicant must be a full-time undergraduate, master's, or Ph.D. student and register the APNET25 conference.</li>
        <li><strong>Young Scholars:</strong> The applicant must be an early-career researcher (within three years of completing their Ph.D.) affiliated with a non-top-tier university. Young scholars are also required to register this conference.</li>
    </ul>
    <br>
    <h3>Application</h3>
    <br>
    <p style="text-align: justify; text-indent: 2em;">Applications have to be submitted by email to: <a href="mailto:apnet2025-travelgrant@outlook.com">apnet2025-travelgrant@outlook.com</a> The "Subject" line should contain "APNet 2025 grant application" and the applicant's name. The following information is requested from all applicants:</p>
    <br>
    <ul class="custom-list">
        <li>Author (only one): give your name, email, and affiliation.</li>
        <li>Participation in APNet 2025: describe briefly your participation in APNet.</li>
        <li>Motivation statement: a short explanation of the expected benefits from attending the conference and why the grant should be awarded. The statement should describe ongoing research and recent papers published by the applicant. It should be written in English and no longer than one page.</li>
        <li>A letter of recommendation from the advisor supporting the application. Young scholars do not need a recommendation letter.</li>
        <li>Please indicate whether your paper has been granted support by the ABR program. Applications that do not include this declaration will be disqualified by the travel grant committee.</li>
    </ul>
    <br>
    <h2>Diversity & Inclusion Travel Grant Program</h2>
    
    <h3>Eligibility</h3>
    <br>
    <p style="text-align: justify; text-indent: 2em;">In addition to meeting the general eligibility requirements of the Standard Travel Grant Program, applicants for the Diversity & Inclusion Travel Grant Program must also meet the following criterion:</p>
    <br>
    <ul>
        <li>Be a citizen or current resident of one of the following countries:
Vietnam, Thailand, Philippines, Indonesia, Malaysia, Cambodia, Laos, Myanmar, or other countries in Southeast Asia.
</li>
    </ul>
    <br>
    <h3>Application</h3>
    <br>
    <p style="text-align: justify; text-indent: 2em;">Applications have to be submitted by email to <a href="mailto:zhangax@connect.ust.hk">zhangax@connect.ust.hk</a>. The required information is the same as that for the Standard Travel Grant Program. </p>
    <br>
    <h2>Selection Process</h2>
    <br>
    <ul class="custom-list">
        <li>The Travel Grant Chairs will review applications based on academic merit, financial need, and potential contributions to the conference. </li>
        <li>Preference will be given to applicants who have not sufficient financial supports and is really interested in networking research.</li>
        <li>Efforts will be made to ensure a fair and diverse selection of recipients.</li>
    </ul>
    <br>
    <h2>Note</h2>
    <br>
    <p style="text-align: justify; text-indent: 2em;">Receivers of an APNet Grant will register with a special promotion code. Therefore, applicants shall not register for the conference until they have received the notification of acceptance from the grant chair (sent before the early registration deadline).</p>
      <br>
    <p style="text-align: justify; text-indent: 2em;">We encourage all eligible participants to apply and look forward to welcoming you to the conference!</p>






